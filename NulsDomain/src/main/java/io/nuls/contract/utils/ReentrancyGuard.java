package io.nuls.contract.utils;

import io.nuls.contract.role.Ownable;

import static io.nuls.contract.sdk.Utils.revert;

/**
 * @author: PierreLuo
 * @date: 2024/10/24
 */
public class ReentrancyGuard extends Ownable {
    // Booleans are more expensive than uint256 or any type that takes up a full
    // word because each write operation emits an extra SLOAD to first read the
    // slot's contents, replace the bits taken up by the boolean, and then write
    // back. This is the compiler's defense against contract upgrades and
    // pointer aliasing, and it cannot be disabled.

    // The values being non-zero value makes deployment a bit more expensive,
    // but in exchange the refund on every call to nonReentrant will be lower in
    // amount. Since refunds are capped to a percentage of the total
    // transaction's gas, it is best to keep them low in cases like this one, to
    // increase the likelihood of the full refund coming into effect.
    private final static int NOT_ENTERED = 1;
    private final static int ENTERED = 2;

    private int _status;

    /**
     * @dev Unauthorized reentrant call.
     */
    public ReentrancyGuard() {
        _status = NOT_ENTERED;
    }

    /**
     * @dev Prevents a contract from calling itself, directly or indirectly.
     * Calling a `nonReentrant` function from another `nonReentrant`
     * function is not supported. It is possible to prevent this from happening
     * by making the `nonReentrant` function external, and making it call a
     * `private` function that does the actual work.
     */
    protected void _nonReentrantBefore() {
        // On the first call to nonReentrant, _status will be NOT_ENTERED
        if (_status == ENTERED) {
            revert("ReentrancyGuardReentrantCall");
        }

        // Any calls to nonReentrant after this point will fail
        _status = ENTERED;
    }

    protected void _nonReentrantAfter() {
        // By storing the original value once again, a refund is triggered (see
        // https://eips.ethereum.org/EIPS/eip-2200)
        _status = NOT_ENTERED;
    }

    /**
     * @dev Returns true if the reentrancy guard is currently set to "entered", which indicates there is a
     * `nonReentrant` function in the call stack.
     */
    private boolean _reentrancyGuardEntered() {
        return _status == ENTERED;
    }
}
